int	ft_is_prime(int nb)
{
	int	i;
	int	s;

	s = 0;
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
			s++;
		i++;
	}
	if (s == 0 && !(nb < 2))
		return (1);
	else
		return (0);
}
